const gsocSet = new Set(GSOC_ORGS.map((o) => o.toLowerCase()));
const lfxSet  = new Set(LFX_ORGS.map((o) => o.toLowerCase()));

export type OrgTag = "gsoc" | "lfx" | "both" | "none";

export function getOrgTag(orgLogin: string): OrgTag {
  const key = orgLogin.toLowerCase();
  const inGsoc = gsocSet.has(key);
  const inLfx  = lfxSet.has(key);

  if (inGsoc && inLfx) return "both";
  if (inGsoc)          return "gsoc";
  if (inLfx)           return "lfx";
  return "none";
}
